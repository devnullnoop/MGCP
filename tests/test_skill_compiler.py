class TestSkillLookup:
    @pytest.mark.asyncio
    async def test_find_skill_path_returns_none_when_absent(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        path = find_skill_path("session_end", scope="user")
        assert path is None

    @pytest.mark.asyncio
    async def test_find_skill_path_returns_path_after_compile(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        await compile_intent_to_skill("session_end", store, scope="user")
        path = find_skill_path("session_end", scope="user")
        assert path is not None
        assert path.exists()

    @pytest.mark.asyncio
    async def test_is_skill_stale_returns_none_when_absent(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        result = await is_skill_stale("session_end", store, scope="user")
        assert result is None  # not "stale", just absent

    @pytest.mark.asyncio
    async def test_recompile_overwrites_cleanly(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        first = await compile_intent_to_skill("session_end", store, scope="user")
        first_size = first.bytes_written
        # Recompile
        second = await compile_intent_to_skill("session_end", store, scope="user")
        # Same path, similar size (timestamp may shift a few bytes)
        assert first.skill_path == second.skill_path
        assert abs(first_size - second.bytes_written) < 100


# ---------------------------------------------------------------------------
# The compile-does-not-modify-source invariant (anti-Phase-8)
# ---------------------------------------------------------------------------


class TestImmutability:
    @pytest.mark.asyncio
    async def test_compile_does_not_remove_intent_from_config(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        """The whole point of v2.3: compiling a skill must NOT remove the
        source intent from intent_config.json. Phase 8's failure was hiding
        knowledge from active query — this test enforces that we don't."""
        store = LessonStore(db_path=temp_db)
        from mgcp.intent_config import load_config

        before = {i.name for i in load_config().intents}
        await compile_intent_to_skill("session_end", store, scope="user")
        after = {i.name for i in load_config().intents}
        assert before == after
        assert "session_end" in after

    @pytest.mark.asyncio
    async def test_compile_does_not_remove_lessons(self, isolated_dirs, temp_db):
        """Same invariant for lessons: backing lessons stay queryable."""
        store = LessonStore(db_path=temp_db)
        await store.add_lesson(Lesson(
            id="must-stay-queryable",
            trigger="x",
            action="y",
            rationale="z",
            tags=["test"],
        ))
        workflow = Workflow(
            id="wf",
            name="WF",
            description="d",
            trigger="t",
            steps=[
                WorkflowStep(
                    id="s",
                    name="S",
                    description="d",
                    order=1,
                    lessons=[
                        WorkflowStepLesson(
                            lesson_id="must-stay-queryable",
                            relevance="r",
                            priority=1,
                        ),
                    ],
                ),
            ],
        )
        await store.save_workflow(workflow)
        intent = IntentDefinition(
            name="i",
            description="d",
            action="a",
            linked_workflow="wf",
        )
        save_config(IntentConfig(version=1, intents=[intent]))

        await compile_intent_to_skill("i", store, scope="user")

        # The lesson must still be retrievable via get_lesson
        lesson = await store.get_lesson("must-stay-queryable")
        assert lesson is not None
        assert lesson.id == "must-stay-queryable"
