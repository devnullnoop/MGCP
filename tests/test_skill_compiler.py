"""Tests for skill_compiler — intent → SKILL.md compilation.

These tests verify the v2.3 successor to the deleted Phase 8 skill compilation.
The key invariants:
- Compiling does NOT modify the source intent or its lessons
- Same input always produces a parseable, well-formed SKILL.md
- Workflow inlining walks intent → workflow → steps → lessons in order
- Missing/deleted workflows and lessons degrade gracefully
- User-scope and project-scope writes go to different paths
"""

import os
import tempfile
from pathlib import Path

import pytest

from mgcp.intent_config import (
    DEFAULT_INTENTS,
    IntentConfig,
    IntentDefinition,
    save_config,
)
from mgcp.models import Lesson, Workflow, WorkflowStep, WorkflowStepLesson
from mgcp.persistence import LessonStore
from mgcp.skill_compiler import (
    CompileResult,
    compile_intent_to_skill,
    find_skill_path,
    is_skill_stale,
)


