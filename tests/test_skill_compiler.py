class TestScopes:
    @pytest.mark.asyncio
    async def test_user_scope_writes_to_home(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        result = await compile_intent_to_skill("session_end", store, scope="user")
        # Path should be under the isolated HOME
        assert str(result.skill_path).startswith(os.environ["HOME"])
        assert ".claude/skills/session_end/SKILL.md" in str(result.skill_path)

    @pytest.mark.asyncio
    async def test_project_scope_writes_to_project_path(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        project_root = isolated_dirs / "fake-project"
        project_root.mkdir()
        result = await compile_intent_to_skill(
            "session_end", store, scope="project", project_path=project_root
        )
        assert str(result.skill_path).startswith(str(project_root))
        assert ".claude/skills/session_end/SKILL.md" in str(result.skill_path)

    @pytest.mark.asyncio
    async def test_invalid_scope_raises(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        with pytest.raises(ValueError, match="scope must be"):
            await compile_intent_to_skill("session_end", store, scope="globally")

    @pytest.mark.asyncio
    async def test_unknown_intent_raises(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
