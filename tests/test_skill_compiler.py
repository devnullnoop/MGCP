# ---------------------------------------------------------------------------
# Compilation without a linked workflow (action-only)
# ---------------------------------------------------------------------------


class TestCompileWithoutWorkflow:
    @pytest.mark.asyncio
    async def test_compiles_intent_with_no_linked_workflow(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        """An intent without linked_workflow still produces a valid SKILL.md."""
        store = LessonStore(db_path=temp_db)
        result = await compile_intent_to_skill("session_end", store, scope="user")

        assert isinstance(result, CompileResult)
        assert result.intent_name == "session_end"
        assert result.workflow_id is None
        assert result.step_count == 0
        assert result.lesson_count == 0
        assert result.has_gate is True  # session_end has a keyword gate
        assert result.skill_path.exists()
        assert result.bytes_written > 0

    @pytest.mark.asyncio
    async def test_skill_file_has_required_frontmatter(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        result = await compile_intent_to_skill("session_end", store, scope="user")
        body = result.skill_path.read_text()

        assert body.startswith("---\n")
        assert "name: session_end" in body
        assert "description:" in body
        assert "---\n" in body[4:]  # closing frontmatter delimiter

    @pytest.mark.asyncio
    async def test_skill_file_has_generation_header(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        """The do-not-edit comment must be present."""
        store = LessonStore(db_path=temp_db)
        result = await compile_intent_to_skill("git_operation", store, scope="user")
        body = result.skill_path.read_text()

        assert "Generated by MGCP skill_compiler" in body
        assert "DO NOT EDIT" in body
        assert "intent 'git_operation'" in body

    @pytest.mark.asyncio
    async def test_gate_message_renders_as_hard_rule(
        self, isolated_dirs, temp_db, seeded_intent_config
    ):
        store = LessonStore(db_path=temp_db)
        result = await compile_intent_to_skill("git_operation", store, scope="user")
        body = result.skill_path.read_text()

        assert "## Hard rule" in body
        assert "STOP" in body or "git rules" in body


# ---------------------------------------------------------------------------
# Compilation WITH a linked workflow (the full chain)
# ---------------------------------------------------------------------------


class TestCompileWithWorkflow:
    @pytest.mark.asyncio
    async def test_compiles_full_chain(self, isolated_dirs, temp_db):
        """Add an intent with linked_workflow → workflow with steps → lessons.
        Then compile and verify everything got inlined."""
        store = LessonStore(db_path=temp_db)

        # Seed two lessons that the workflow steps will reference
        await store.add_lesson(Lesson(
            id="run-linter",
            trigger="commit, push",
            action="Run ruff check src/ tests/ before committing.",
            rationale="Pushing lint errors wastes CI time.",
            tags=["git", "linting"],
        ))
        await store.add_lesson(Lesson(
            id="run-tests",
            trigger="commit, push",
            action="Run pytest -m 'not slow' before pushing.",
            rationale="CI failures after push are avoidable.",
            tags=["git", "testing"],
        ))

        # Build a 2-step workflow that links those lessons
        workflow = Workflow(
            id="pre-commit-discipline",
            name="Pre-commit Discipline",
            description="Verification steps before any git commit",
            trigger="commit, push",
            steps=[
                WorkflowStep(
                    id="lint",
                    name="Lint",
                    description="Run the linter to catch style violations",
                    order=1,
                    lessons=[
                        WorkflowStepLesson(
                            lesson_id="run-linter",
                            relevance="Linter must pass before commit",
                            priority=1,
                        ),
                    ],
                ),
                WorkflowStep(
                    id="test",
                    name="Test",
                    description="Run the test suite",
                    order=2,
                    lessons=[
                        WorkflowStepLesson(
                            lesson_id="run-tests",
                            relevance="Tests must pass before push",
                            priority=1,
                        ),
                    ],
                ),
            ],
        )
        await store.save_workflow(workflow)

        # Build an intent that links to the workflow
        intent = IntentDefinition(
            name="custom_git_op",
            description="commit, push, ship",
            action="Run pre-commit discipline before any git command",
            tags=["git"],
            linked_workflow="pre-commit-discipline",
        )
        save_config(IntentConfig(version=1, intents=[intent]))

        result = await compile_intent_to_skill("custom_git_op", store, scope="user")

        assert result.workflow_id == "pre-commit-discipline"
        assert result.step_count == 2
        assert result.lesson_count == 2

        body = result.skill_path.read_text()
        # Workflow header
        assert "Pre-commit Discipline" in body
        # Step headers in order
        lint_idx = body.find("Step 1: Lint")
        test_idx = body.find("Step 2: Test")
        assert lint_idx > 0
        assert test_idx > lint_idx, "Steps should appear in order"
        # Lesson actions inlined
        assert "Run ruff check" in body
        assert "Run pytest" in body
        # Lesson rationale (Why) inlined
        assert "wastes CI time" in body
        # Priority labels rendered
        assert "CRITICAL" in body

    @pytest.mark.asyncio
    async def test_missing_workflow_degrades_gracefully(
        self, isolated_dirs, temp_db
    ):
        store = LessonStore(db_path=temp_db)
        intent = IntentDefinition(
            name="orphan_intent",
            description="trigger phrase",
            action="do the thing",
            linked_workflow="nonexistent-workflow",
        )
        save_config(IntentConfig(version=1, intents=[intent]))

        result = await compile_intent_to_skill("orphan_intent", store, scope="user")
        assert result.step_count == 0
        assert result.lesson_count == 0
        body = result.skill_path.read_text()
        assert "not found" in body  # the graceful fallback message

    @pytest.mark.asyncio
    async def test_missing_lesson_degrades_gracefully(self, isolated_dirs, temp_db):
        store = LessonStore(db_path=temp_db)
        # Workflow references a lesson that doesn't exist
        workflow = Workflow(
            id="wf-missing-lesson",
            name="Missing Lesson WF",
            description="Tests handling of dangling lesson references",
            trigger="test",
            steps=[
                WorkflowStep(
                    id="step1",
                    name="Step",
                    description="A step",
                    order=1,
                    lessons=[
                        WorkflowStepLesson(
                            lesson_id="lesson-that-doesnt-exist",
                            relevance="N/A",
                            priority=1,
                        ),
                    ],
                ),
            ],
        )
        await store.save_workflow(workflow)
        intent = IntentDefinition(
            name="dangling_intent",
            description="x",
            action="y",
            linked_workflow="wf-missing-lesson",
        )
        save_config(IntentConfig(version=1, intents=[intent]))

        result = await compile_intent_to_skill("dangling_intent", store, scope="user")
        assert result.step_count == 1
        assert result.lesson_count == 0  # the dangling lesson doesn't count
        body = result.skill_path.read_text()
        assert "missing lesson" in body  # the graceful fallback


